
package testlocation;


public class Location {
    
    public int row;
    public int col;
    public double maxVal;
    
    public Location(int row, int col, double maxVal){
        this.row = row;
        this.col = col;
        this.maxVal = maxVal;
        
        
    }
    


    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

    public double getMaxVal() {
        return maxVal;
    }

   
    
    
}
